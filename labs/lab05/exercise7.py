import math

number = float(input("Gimme one number: "))

square_root = math.sqrt(number)
square = number ** 2
cube = number ** 3
sin_value = math.sin(number) 

print("Number: " , number)
print("Square root: " , square_root)
print("Square : " , square)
print("Cube: " , cube)
print("Sine Rad: " , sin_value)
